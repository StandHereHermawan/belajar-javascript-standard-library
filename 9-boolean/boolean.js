/**
 * Boolean
 */
{
    const bool = Boolean(true);

    console.info(bool);
    console.info(typeof bool);
    console.info(bool.toString());
    console.info(bool.valueOf());
}

// Menggunakan kata kunci New
{
    const bool = new Boolean(true);

    console.info(bool);
    console.info(typeof bool);
    console.info(bool.toString());
    console.info(bool.valueOf());
}
